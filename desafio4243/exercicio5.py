# Faça um programa que calcule a área de um quadrado/retângulo. ​


base = float(input("Digite o valor da base do retângulo/quadrado: "))
altura = float(input("Digite o valor da altura do retângulo/quadrado: "))

area = base * altura
area = round(area, 2)

print("A area do retangulo/quadrado é de: ", area)