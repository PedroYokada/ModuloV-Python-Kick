# Inteiro
num_inteiro = 42
print("Inteiro:", num_inteiro)
 
# Ponto Flutuante
num_ponto_flutuante = 3.14159
print("Ponto Flutuante:", num_ponto_flutuante)
 
# # String
texto = "Olá, Mundo!"
print("String:", texto)
 
# # Booleano
booleano_verdadeiro = True
booleano_falso = False
print("Booleano Verdadeiro:", booleano_verdadeiro)
print("Booleano Falso:", booleano_falso)
 
# Lista
lista = [1, 2, 3, 4, 5]
print("Lista:", lista)
 
# Tupla
tupla = (10, 20, 30)
print("Tupla:", tupla)
 
# Dicionário
dicionario = {"nome": "Alice", "idade": 30, "cidade": "São Paulo"}
print("Dicionário:", dicionario)
 
# Conjunto
conjunto = {1, 2, 3, 4, 5}
print("Conjunto:", conjunto)