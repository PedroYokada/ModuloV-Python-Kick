def calcular_area_retangulo(largura,altura):
  area = largura * altura
  return area

largura = 5
altura = 10
area = calcular_area_retangulo(largura,altura)
print(f"A area do retangulo de largura {largura} e altura {altura} é {area}")

largura = 7.5
altura = 3.2
area = calcular_area_retangulo(largura,altura)
print(f"A area do retangulo de largura {largura} e altura {altura} é {area}")